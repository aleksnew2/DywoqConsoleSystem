namespace DywoqConsoleSystem;

/// <summary>
/// Defines the different types of console messages.
/// </summary>
public enum ConsoleMessageType
{
  /// <summary>
  /// Represents an error message.
  /// </summary>
  ERROR,

  /// <summary>
  /// Represents an informational message.
  /// </summary>
  INFO,

  /// <summary>
  /// Represents a warning message.
  /// </summary>
  WARNING,

  /// <summary>
  /// Represents a debug message.
  /// </summary>
  DEBUG
}
